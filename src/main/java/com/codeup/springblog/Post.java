package com.codeup.springblog;

class Post {
    private String title;
    private String body;

    String getTitle() {
        return title;
    }
    void setTitle(String title){
        this.title = title;
    }
    String getBody(){
        return body;
    }
    void setBody(String body){
        this.body = body;
    }
}
